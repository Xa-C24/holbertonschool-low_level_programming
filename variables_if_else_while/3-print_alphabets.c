#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - Prints the last digit of a randomly generated number
 *        and whether it is greater than 5, less than 6, or 0.
 *
 * Return: Always 0.
 */
int main(void)
{
	char az;
	char AZ;

	for (az = 'a'; az <= 'z'; az++);

	for (AZ = 'A' ; AZ <= 'Z' ; AZ++);

putchar(az);
putchar(AZ);
putchar('\n');

	return (0);
}
