#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Prints the last digit of a randomly generated number
 *        and whether it is greater than 5, less than 6, or 0.
 *
 * Return: Always 0.
 */
int main(void)
{
char c;

for (c = 'a'; c <= 'z'; c++);
{
putchar(c);
}
for (c = 'A' ; c <= 'Z'; c++);
{
putchar(c);
}
putchar('\n');
return (0);
}
